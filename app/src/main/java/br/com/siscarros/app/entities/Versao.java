package br.com.siscarros.app.entities;

public class Versao {
	
	private Long id;
	private String nomeVersao;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNomeVersao() {
		return nomeVersao;
	}
	public void setNomeVersao(String nomeVersao) {
		this.nomeVersao = nomeVersao;
	}
	
	

}
